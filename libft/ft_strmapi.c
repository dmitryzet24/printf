#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*new_str;
	size_t			length;
	unsigned int	i;

	if (!s || !f)
		return (NULL);
	length = ft_strlen(s) + 1;
	new_str = (char *)malloc(sizeof(char) * (length));
	if (!new_str)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		new_str[i] = f(i, s[i]);
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
